void main()
{
    // integers and math
    int sum;
    sum = (35 + 1) / (36 * 1) * (1 & 1);
    cout << sum << endl;

    // cout statements
    cout << endl << 0 << sum << 0 << endl;

    // plus equals & minus equals
    int test;
    test = 0;
    cout << test << endl;
    test += 99;
    cout << test << endl;
    test -= 99;
    cout << test << endl;

    // while loops
    int max;
    max = 10;
    while (max >= 0)
    {
        cout << max << endl;
        max -= 1;
    }
}
